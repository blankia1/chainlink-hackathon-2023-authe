import os
import json
import logging
from web3_input_decoder import decode_constructor, decode_function
import re

LOGGER = logging.getLogger()
LOGGER.setLevel(logging.INFO)
LOGGER.addHandler(logging.StreamHandler())

def lambda_handler(event, context):
    if event.get('body'):
        body = json.loads(event["body"])
        from_address = body['f'].lower() 
        to_address =  body['t'].lower() 
        function_name =  body['n']
        encoded_data =  body['d']
        resource_address =  body.get('r').lower()  # contract address
    else:
        from_address = event['f'].lower() 
        to_address =  event['t'].lower() 
        function_name =  event['n']
        encoded_data =  event['d']
        resource_address =  event.get('r').lower()  # contract address

    try:
        # ProxyAbi
        LOGGER.info("Trying to decode with proxyAbi")
        decoded_data = decode_function(
            proxy_abi, encoded_data
        )

        # The proxy abi contains more meta information
        function_name_pat = r'(?<=_functionName\', \').+?(?=\')'  # Expression to catch "'_functionName', 'transfer'),""
        function_name = re.findall(function_name_pat, str(decoded_data))[0] # Expression to catch "'_functionName', 'transfer'),"

        address_pat = r'(?<=address\', \').+?(?=\')'  # Expression to catch "'address', 'transfer'),""
        resource_address = re.findall(address_pat, str(decoded_data))[0] # Expression to catch "'address', '0x'),"

        call_data_pat = r'(?<=_data\', b\').+?(?=\')'  # Expression to catch call_data "'_data', b'xa9'),"
        raw_call_data = re.findall(call_data_pat, str(decoded_data))[0]
        raw_call_data_encoded_in_bytes = bytes(raw_call_data, encoding='utf8')
        raw_call_data_encoded_in_bytes = raw_call_data_encoded_in_bytes.decode('unicode-escape').encode('ISO-8859-1')
        call_data = raw_call_data_encoded_in_bytes.hex()
        nested_decoded_data = decode_function(
            custom_erc20abi, call_data
        )
        decoded_data.append("0x" + call_data) # enrich with the nested raw call data
        decoded_data.append(nested_decoded_data) # enrich with the decoded nested raw call data

        try:
            LOGGER.info("Trying to find the to address in the decoded data")
            # Change the to address to the information based in the call data
            to_pat = r'(?<=receiver\', \').+?(?=\')'  # Expression to catch receiver "'receiver', b'xa9'),"
            found_to_address = re.findall(to_pat, str(nested_decoded_data))[0]
            if found_to_address: 
                LOGGER.info("found to address: " + found_to_address)
                to_address = found_to_address
        except Exception as err:
            print(err)
    except Exception as err:
        LOGGER.info("Trying to decode with custom erc20ABI")
        # ERC20ABI
        decoded_data = decode_function(
            custom_erc20abi, encoded_data
        )
        try:
            LOGGER.info("Trying to find the to address in the decoded data")
            # Change the to address to the information based in the call data
            to_pat = r'(?<=receiver\', \').+?(?=\')'  # Expression to catch receiver "'receiver', b'xa9'),"
            found_to_address = re.findall(to_pat, str(decoded_data))[0]
            if found_to_address: 
                LOGGER.info("found to address: " + found_to_address)
                to_address = found_to_address
        except Exception as err:
            print(err)

    LOGGER.info("Decoded data is: " + str(decoded_data))

    json_data = {
        'f' : from_address,
        't' : to_address,
        'n' : function_name,
        'd' : json.dumps(str(decoded_data)),
        'r' : resource_address
    } 
    
    return {
        'statusCode': 200,
        'body': json.dumps(json_data),
    }


